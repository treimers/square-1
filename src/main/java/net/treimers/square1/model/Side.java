package net.treimers.square1.model;

/**
 * This enum represents the sides a of Square-1 piece.
 */
public enum Side {
	/** The top side. */
	TOP,
	/** The left side. */
	LEFT,
	/** The front side. */
	FRONT,
	/** The right side. */
	RIGHT,
	/** The back side. */
	BACK,
	/** The bottom side. */
	BOTTOM,
	/** The inner vertical side(s). */
	INNER_VERTICAL,
	/** The inner horizontal side(s). */
	INNER_HORIZONTAL;
}
